    private void cmdsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdsimpanActionPerformed

        String jk = "";
        if(rdlaki.isSelected()){
            jk = "Laki-Laki";
        }else if(rdperempuan.isSelected()){
            jk = "Perempuan";
        }
        
        if(txtnis.getText().equals("")||txtnama.getText().equals("")||jk.equals("")||cmdjurusan.getSelectedItem().toString().equals("")){
            JOptionPane.showMessageDialog(null, "Harap di isi Semua");
        }else{
            String query = "INSERT INTO siswa VALUES ('"+ txtnis.getText() + "','"
                    + txtnama.getText() + "','"+jk+"','"+cmdjurusan.getSelectedItem().toString() + "','"
                    + txtalamat.getText() +"')";
            manData(query, "Data berhasil di tambahkan", "Data gagal di tambahkan");
        }
        
    }//GEN-LAST:event_cmdsimpanActionPerformed

    private void cmdubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdubahActionPerformed
        
        String jk = "";
        if(rdlaki.isSelected()){
            jk = "Laki-Laki";
        }else if(rdperempuan.isSelected()){
            jk = "Perempuan";
        }
        if(jtabel.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Pilih data yang akan di ubah");
        }else{
            if(txtnis.getText().equals("")||txtnama.getText().equals("")||jk.equals("")||cmdjurusan.getSelectedItem().toString().equals("")){
                JOptionPane.showMessageDialog(null, "Harap di isi Semua");
            }else{
                DefaultTableModel tabel = (DefaultTableModel) jtabel.getModel();
                String query = "UPDATE siswa SET nis='"+ txtnis.getText() 
                        + "', nama='" + txtnama.getText() + "',jenkel='"+jk
                        +"',jurusan='"+cmdjurusan.getSelectedItem().toString() 
                        + "', alamat='"+ txtalamat.getText() 
                        +"' WHERE nis='"+ tabel.getValueAt(jtabel.getSelectedRow(), 0).toString() + "'";
                manData(query, "Data berhasil di ubah", "Data gagal di ubah");
            }
        }
    }//GEN-LAST:event_cmdubahActionPerformed

    private void jtabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtabelMouseClicked

        DefaultTableModel tabel = (DefaultTableModel) jtabel.getModel();
        txtnis.setText(tabel.getValueAt(jtabel.getSelectedRow(), 0).toString());
        txtnama.setText(tabel.getValueAt(jtabel.getSelectedRow(), 1).toString());
        if(tabel.getValueAt(jtabel.getSelectedRow(), 2).toString().equals("Laki-Laki")){
            rdlaki.setSelected(true);
        }else{
            rdperempuan.setSelected(true);
        }
        cmdjurusan.setSelectedItem(tabel.getValueAt(jtabel.getSelectedRow(), 3).toString());
        txtalamat.setText(tabel.getValueAt(jtabel.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_jtabelMouseClicked

